//------------------------------------------------------------------------------

// Använd en enkellänkad lista definierad i klassen List
//------------------------------------------------------------------------------
#include <iostream>
#include <iomanip>
using namespace std;
#include "list.h"
int main()
{
//------------------------------------------------------------------------------
// Skapa en tom lista
//------------------------------------------------------------------------------
  List list;

//------------------------------------------------------------------------------
// Lägg i värden i listan. Skriv sedan ut listan
//------------------------------------------------------------------------------
  for(int i=1; i<=4; i++)
    list.putFirst(12*i);

  list.showList();
//------------------------------------------------------------------------------
// Sätt in värdet '13' efter värdet '24'
//------------------------------------------------------------------------------
  list.insAfter(36,13);
  list.showList();

//------------------------------------------------------------------------------
// Sätt in värdet '134' sist i listan
//------------------------------------------------------------------------------
  list.putLast(134);
  list.showList();



  return 0;
}
